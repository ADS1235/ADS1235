
       public class EvenOddNumber {

       public void EvenOdd(int n) {
		
		if(n%2==0)
		{
		System.out.println("Even Number");
		}
		else
		System.out.println("Odd Number");
		}
	    
		  public static void main(String[] args) {
		  EvenOddNumber EON= new EvenOddNumber();
		    	        EON.EvenOdd(5);
        }
	       }
